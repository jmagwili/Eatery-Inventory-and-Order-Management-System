    public final void setMenuTbl(ResultSet rs){
        DefaultTableModel dm = (DefaultTableModel) menuTbl.getModel();
        int rowCount = dm.getRowCount();
        
        for (int i = rowCount - 1; i >= 0; i--) {
            dm.removeRow(i);
        }
        
        try{     
            while(rs.next()){
                String categoryID = rs.getString("menuID");
                String category = rs.getString("menu");
                String type = rs.getString("type");
                
                String tblData[] = {categoryID,category,type};
                DefaultTableModel tblModel = (DefaultTableModel)menuTbl.getModel();
                tblModel.addRow(tblData);
            }
            
        }catch(SQLException e){
            System.out.println(e);
        }
    }
        
