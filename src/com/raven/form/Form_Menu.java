    private void ingredientAddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingredientAddBtnActionPerformed
        String menu = menuTextField.getText();
        String type = typeComboBox.getSelectedItem().toString();
        
        if(!"".equals(menu)){
            if(this.menu.checkDuplicate(menu)){
                this.menu.add(menu, type);
                menuTextField.setText("");
                setMenuTbl(this.menu.getAll());
                JOptionPane.showMessageDialog(this, "Menu successfully updated.");
            }else{
                JOptionPane.showMessageDialog(this, "Menu has already been added.");
            }  
        }else{
            JOptionPane.showMessageDialog(this, "Please fill-up all fields.");
            System.out.println("empty field detected");
        }    
    }//GEN-LAST:event_ingredientAddBtnActionPerformed

    private void ingredientUpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingredientUpdateBtnActionPerformed
        String menu = menuTextField.getText();
        String type = typeComboBox.getSelectedItem().toString();
        
        if(!"".equals(menu) && menuID != -1){
            this.menu.update(menuID,menu,type);
            setMenuTbl(this.menu.getAll());
            menuTextField.setText("");
            menuID = -1;
            JOptionPane.showMessageDialog(this, "Menu successfully updated.");
        }else{
            System.out.println("empty field detected");
            JOptionPane.showMessageDialog(this, "Please fill-up all fields.");
        }  
    }//GEN-LAST:event_ingredientUpdateBtnActionPerformed

    private void modernButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modernButton2ActionPerformed
        String menu = menuTextField.getText();
        String type = typeComboBox.getSelectedItem().toString();
        
        ResultSet searchResult = this.menu.search(menu, type);
        setMenuTbl(searchResult);
    }//GEN-LAST:event_modernButton2ActionPerformed

    private void modernButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modernButton1ActionPerformed
        if(menuID != -1){
            int response = JOptionPane.showConfirmDialog(null, 
            "Are you sure you want to delete this menu?", 
            "Warning", 
            JOptionPane.YES_NO_OPTION, 
            JOptionPane.WARNING_MESSAGE);

            if (response == JOptionPane.YES_OPTION){
                menu.delete(menuID);
                setMenuTbl(menu.getAll());
                menuTextField.setText("");
                menuID = -1;
                JOptionPane.showMessageDialog(this, "Menu successfully deleted.");
            }      
        }else{
            System.out.println("No selected field");
        }
        
    }//GEN-LAST:event_modernButton1ActionPerformed

    private void menuTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_menuTblMouseClicked
        int selectedRow = menuTbl.getSelectedRow();
        menuID = Integer.parseInt(menuTbl.getValueAt(selectedRow, 0).toString());
        menuTextField.setText(menuTbl.getValueAt(selectedRow, 1).toString());
        typeComboBox.setSelectedItem(menuTbl.getValueAt(selectedRow, 2));
    }//GEN-LAST:event_menuTblMouseClicked


